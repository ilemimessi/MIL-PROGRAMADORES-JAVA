package modulo1;

public class Ejercicio10 {
	public static void main(String[] args) {
		System.out.println("Este es el ejercicio n°10");
		 
		//INCREMENTO
		int valor;
		valor = 10;
		valor++; //valor = valor+1;
		System.out.println(valor);
		
		//DECREMENTO
		--valor;
		System.out.println(valor);
		
	}
}
